#include "libft.h"

int		ft_nbmot(char const *s, char c)
{
	int		i;
	int		nb;

	nb = 0;
	i = -1;
	while (s[++i])
		if ((s[i] == c && s[i + 1] != c && s[i + 1]) || (i == 0 && s[i] != c))
			nb++;
	return (nb);
}
